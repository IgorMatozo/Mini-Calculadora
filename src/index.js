// Função para adição
function adicao(a, b) {
    return a + b;
}

// Função para subtração
function subtracao(a, b) {
    return a - b;
}

// Função para multiplicação
function multiplicacao(a, b) {
    return a * b;
}

// Função para divisão
function divisao(a, b) {
    if (b === 0) {
        return "Não é possível dividir por zero!";
    } else {
        return a / b;
    }
}
